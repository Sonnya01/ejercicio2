
public class Funciones {

    /**
     * Divide dos numeros, el numerador entre el denominador.
     *
     * @param a numerador
     * @param b denominador
     * @return el resultado de la division.
     */
    public double dividir(int a, int b) {
        return a == 0.0 ? -1.0 : a * 1.0 / b * 1.0;
    }
}
